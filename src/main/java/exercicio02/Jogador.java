package exercicio02;

public class Jogador {
    private String nome;
    private String sobrenome;
    private int idade;
    private String posicao;
    private String sexo;
    private String time;

    Jogador(String nome, String sobrenome, int idade, String posicao, String sexo, String time) {
        this.nome = nome;
        this.sobrenome = sobrenome;
        this.idade = idade;
        this.posicao = posicao;
        this.sexo = sexo;
        this.time = time;
    }
}
